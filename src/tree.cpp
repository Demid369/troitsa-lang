#include "troitsa/tree.hpp"

namespace Троица {
namespace Древо {

std::string имяТипаУзла(ТипУзла тип) {
    switch (тип) {
        // Значения
        case ТипУзла::ЗначениеЧисло: return "ЗначениеЧисло";
        case ТипУзла::ЗначениеСтрока: return "ЗначениеСтрока";
        case ТипУзла::ЗначениеСимвол: return "ЗначениеСимвол";
        case ТипУзла::ЗначениеЛогика: return "ЗначениеЛогика";
        
        // Выражения
        case ТипУзла::Имя: return "Имя";
        case ТипУзла::ДвойнаяОперация: return "ДвойнаяОперация";
        case ТипУзла::ОднарнаяОперация: return "ОднарнаяОперация";
        case ТипУзла::Вызов: return "Вызов";
        case ТипУзла::Доступ: return "Доступ";
        case ТипУзла::Индекс: return "Индекс";
        
        // Инструкции
        case ТипУзла::Пусть: return "Пусть";
        case ТипУзла::Присвоить: return "Присвоить";
        case ТипУзла::Если: return "Если";
        case ТипУзла::Выбор: return "Выбор";
        case ТипУзла::Пока: return "Пока";
        case ТипУзла::Для: return "Для";
        case ТипУзла::Цикл: return "Цикл";
        case ТипУзла::Вернуть: return "Вернуть";
        case ТипУзла::Прервать: return "Прервать";
        case ТипУзла::Продолжить: return "Продолжить";
        case ТипУзла::Блок: return "Блок";
        case ТипУзла::ВыражениеИнструкция: return "ВыражениеИнструкция";
        
        // Объявления
        case ТипУзла::ОбъявФункция: return "ОбъявФункция";
        case ТипУзла::ОбъявТип: return "ОбъявТип";
        case ТипУзла::ОбъявЭффект: return "ОбъявЭффект";
        
        // Программа
        case ТипУзла::Программа: return "Программа";
    }
    return "Неизвестный";
}

} // namespace Древо
} // namespace Троица
